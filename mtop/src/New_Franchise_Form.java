    private void checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkActionPerformed
        try {
            conn.setAutoCommit(true);
            Statement s = (Statement) conn.createStatement();
            s.executeQuery("SELECT vehicle.MTOP_NO FROM vehicle JOIN operator ON vehicle.MTOP_NO = operator.MTOP_NO AND vehicle.MTOP_NO = '" + mtop_num.getText() + "'");
            ResultSet rs = s.getResultSet();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "MTOP NO already exists!");
            } else {
                JOptionPane.showMessageDialog(null, "Vacant");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_checkActionPerformed

