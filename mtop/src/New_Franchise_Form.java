    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

        PreparedStatement pst = null;
        PreparedStatement pst2 = null;
        PreparedStatement pst3 = null;
        PreparedStatement pst4 = null;
        PreparedStatement pst5 = null;

        // Initialization
        Date date_paid = paid.getDate();
        Date mp_exp = mp_exp_date.getDate();

        String filling = filling_fee.getText();
        String franchise = franchise_fee.getText();
        String mayors = mayors_permit_fee.getText();

        String mtop = mtop_num.getText();
        String plate_no = plate.getText();
        String cert1 = cert.getText().toUpperCase();
        String chassis_no = chassis.getText().toUpperCase();
        String maker = make.getText().toUpperCase();
        String route1 = route.getText();
        String motor_no = motor.getText();
        String lname = lastname.getText().toUpperCase();
        String fname = firstname.getText().toUpperCase();
        String address1 = address.getText();
        String or_no = or_num.getText();

        String query1 = "INSERT INTO vehicle(MTOP_NO, plate_no, make, chassis_no, motor_no, reg_no, route, status, care_of, mp_expiration_date) values( ? , ? , ? , ? , ? , ?, ?, ?, ?, ? )";
        String query2 = "INSERT INTO operator(lastname, firstname, address, MTOP_NO ) values( ? , ? , ? , ? )";
        String query3 = "INSERT INTO fees(filling_fee, franchise_fee, mayors_permit_fee, or_no, date_paid, MTOP_NO ) values( ? , ? , ? , ? , ? , ? )";
        String query5 = "INSERT INTO transaction_logs(MTOP_NO, firstname, lastname, make, plate_num, chassis_no, motor_no, ddate, type, action) values( ? , ? , ?, ?, ?, ? ,? ,?, ? , ?)";
        try {
            Statement s = (Statement) conn.createStatement();
            int count = 0;
            ResultSet rs = s.executeQuery("SELECT count(vehicle.MTOP_NO) as num FROM vehicle JOIN operator ON vehicle.MTOP_NO = operator.MTOP_NO");
            while (rs.next()) {
                count = rs.getInt("num");
            }
            int limit = Settings.getFranchiseLimit();
            if (count < limit) {

                conn.setAutoCommit(false);

                pst = (PreparedStatement) conn.prepareStatement(query1);
                pst.setString(1, mtop);
                pst.setString(2, plate_no);
                pst.setString(3, maker);
                pst.setString(4, chassis_no);
                pst.setString(5, motor_no);
                pst.setString(6, cert1);
                pst.setString(7, route1);
                pst.setString(8, "For Release");
                pst.setString(9, care_of.getText());
                pst.setString(10, dformat.format(mp_exp).toString());
                pst.execute();

                pst2 = (PreparedStatement) conn.prepareStatement(query2);
                pst2.setString(1, lname);
                pst2.setString(2, fname);
                pst2.setString(3, address1);
                pst2.setString(4, mtop);
                pst2.execute();

                pst3 = (PreparedStatement) conn.prepareStatement(query3);
                pst3.setString(1, filling);
                pst3.setString(2, franchise);
                pst3.setString(3, mayors);
                pst3.setString(4, or_no);
                pst3.setString(5, dformat.format(date_paid).toString());
                pst3.setString(6, mtop);
                pst3.execute();
                
                pst5 = (PreparedStatement) conn.prepareStatement(query5);
                pst5.setString(1, mtop);
                pst5.setString(2, fname);
                pst5.setString(3, lname);
                pst5.setString(4, maker);
                pst5.setString(5, plate_no);
                pst5.setString(6, chassis_no);
                pst5.setString(7, motor_no);
                pst5.setString(8, dformat.format(date_paid).toString());
                pst5.setString(9, "New");
                pst5.setString(10, "Application For MTOP to Operate an MCH Service");
                pst5.execute();

                pst.close();
                pst2.close();
                pst3.close();
                pst5.close();

                conn.commit();
                JOptionPane.showMessageDialog(null,"Successfully Added");
                List.view();
                Clear();

            } else {
                JOptionPane.showMessageDialog(null, "Franchise is Full!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
            try {
                conn.rollback();
            } catch (SQLException ex) {

            }
        }
    }//GEN-LAST:event_saveActionPerformed

